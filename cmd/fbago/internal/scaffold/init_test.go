func TestInitSupportsRemoteGitTemplateWithSubdirAndRef(t *testing.T) {
	dir := t.TempDir()
	repoDir := t.TempDir()
	writeTemplateFile(t, repoDir, "admin/.fbago-template.yaml", "module: github.com/acme/fba-go-template/admin\n")
	writeTemplateFile(t, repoDir, "admin/go.mod", "module github.com/acme/fba-go-template/admin\n\ngo 1.25.0\n")
	writeTemplateFile(t, repoDir, "admin/cmd/api/main.go", `package main

import "github.com/acme/fba-go-template/admin/internal/app"

func main() {
	app.Register(nil)
}
`)
	writeTemplateFile(t, repoDir, "admin/README.md.tmpl", "Generated [[ .Module ]] from [[ .TemplateModule ]]\n")
	runGit(t, repoDir, "init")
	runGit(t, repoDir, "add", ".")
	runGit(t, repoDir, "-c", "user.name=fbago", "-c", "user.email=fbago@example.invalid", "commit", "-m", "init")
	runGit(t, repoDir, "tag", "admin/v0.1.0")
	t.Setenv("FBAGO_TEMPLATE_CACHE_DIR", t.TempDir())

	if err := scaffold.Init(scaffold.InitOptions{
		Dir:      dir,
		Module:   "github.com/acme/backend",
		Template: "file://" + filepath.ToSlash(repoDir) + "//admin@admin/v0.1.0",
	}); err != nil {
		t.Fatalf("Init() error = %v", err)
	}

	assertFileContains(t, filepath.Join(dir, "go.mod"), "module github.com/acme/backend")
	assertFileContains(t, filepath.Join(dir, "cmd/api/main.go"), `"github.com/acme/backend/internal/app"`)
	assertFileContains(t, filepath.Join(dir, "README.md"), "Generated github.com/acme/backend from github.com/acme/fba-go-template/admin")
	assertFileNotExists(t, filepath.Join(dir, ".git"))
}

