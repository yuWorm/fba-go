package scaffold

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

type InitOptions struct {
	Dir    string
	Module string
	Force  bool
}

type scaffoldFile struct {
	Path     string
	Template string
}

type templateData struct {
	Module string
}

func Init(opts InitOptions) error {
	module := strings.TrimSpace(opts.Module)
	if module == "" {
		return fmt.Errorf("module name is required")
	}
	if strings.ContainsAny(module, " \t\r\n") {
		return fmt.Errorf("module name must not contain whitespace")
	}
	dir := opts.Dir
	if dir == "" {
		dir = "."
	}

	files := backendFiles()
	if !opts.Force {
		for _, file := range files {
			path := filepath.Join(dir, file.Path)
			if _, err := os.Stat(path); err == nil {
				return fmt.Errorf("%s already exists", path)
			} else if !os.IsNotExist(err) {
				return err
			}
		}
	}

	data := templateData{Module: module}
	for _, file := range files {
		content, err := render(file.Template, data)
		if err != nil {
			return fmt.Errorf("render %s: %w", file.Path, err)
		}
		path := filepath.Join(dir, file.Path)
		if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
			return err
		}
		if err := os.WriteFile(path, content, 0o644); err != nil {
			return err
		}
	}
	return nil
}

func render(source string, data templateData) ([]byte, error) {
	tmpl, err := template.New("scaffold").Parse(source)
	if err != nil {
		return nil, err
	}
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

func backendFiles() []scaffoldFile {
	return []scaffoldFile{
		{Path: "go.mod", Template: goModTemplate},
		{Path: "README.md", Template: readmeTemplate},
		{Path: ".env", Template: envTemplate},
		{Path: "cmd/api/main.go", Template: mainTemplate},
		{Path: "internal/app/register.go", Template: appRegisterTemplate},
		{Path: "internal/app/README.md", Template: appReadmeTemplate},
		{Path: "internal/app/health/module.go", Template: healthModuleTemplate},
	}
}

