const mainTemplate = `package main

import (
	"context"
	"log"

	fba "github.com/yuWorm/fba-go"
	"github.com/yuWorm/fba-go/core/plugin"
	appmodules "{{ .Module }}/internal/app"
)

func main() {
	application, err := newApplication()
	if err != nil {
		log.Fatal(err)
	}
	if err := application.Run(context.Background()); err != nil {
		log.Fatal(err)
	}
}

func newApplication() (fba.Application, error) {
	opts, err := fba.LoadOptionsFromEnv()
	if err != nil {
		return nil, err
	}
	opts = opts.WithDefaults()
	application, err := fba.NewApplication(opts)
	if err != nil {
		return nil, err
	}

	registry := plugin.NewRegistry()
	if err := appmodules.Register(registry); err != nil {
		return nil, err
	}

	pluginContext := plugin.NewContext(plugin.ContextOptions{
		Container: application.Container(),
		Router:    application.HTTP(),
		APIGroup:  application.HTTP().Group(opts.App.APIBasePath),
		Config:    opts,
	})
	if err := registry.RegisterAll(pluginContext); err != nil {
		return nil, err
	}
	plugin.MountRoutes(pluginContext.APIGroup(), pluginContext.Routes(), plugin.WithContainer(pluginContext.Container()))

	return application, nil
}
`

const appRegisterTemplate = `package app

import (
	"github.com/yuWorm/fba-go/core/plugin"
	"{{ .Module }}/internal/app/health"
)

func Register(registry *plugin.Registry) error {
	modules := []plugin.Module{
		health.Module(),
	}
	for _, module := range modules {
		if err := registry.Add(module, plugin.ModeAuto); err != nil {
			return err
		}
	}
	return nil
}
`

const appReadmeTemplate = `# App Modules

This directory is project-owned. Business modules live here so the application can freely change routes, models, repositories, services, migrations, and permissions without forking remote packages.

Reusable capabilities can still be imported as external FBA plugins when they are truly generic.
`

const healthModuleTemplate = `package health

import (
	"github.com/gofiber/fiber/v3"
	"github.com/yuWorm/fba-go/core/plugin"
	"github.com/yuWorm/fba-go/core/response"
)

func Module() plugin.Module {
	return module{}
}

type module struct{}

func (module) Meta() plugin.Meta {
	return plugin.Meta{
		ID:          "health",
		Name:        "Health App Module",
		Version:     "0.1.0",
		Description: "Project-owned health module generated by fbago init.",
	}
}

func (module) Register(ctx plugin.Context) error {
	return plugin.RegisterRoutes(ctx, []plugin.Route{
		plugin.GET("/app/ping", "App ping", ping, plugin.Tags("app")),
	})
}

func ping(c fiber.Ctx) error {
	return c.JSON(response.Success(map[string]string{
		"status": "ok",
	}))
}
`
